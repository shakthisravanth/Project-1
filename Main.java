public class Main {
    public static void main(String[] args) {
        int a = 10, b = 5;

        System.out.println("Add: " + (a + b));
        System.out.println("Sub: " + (a - b));
        System.out.println("Mul: " + (a * b));
        System.out.println("Div: " + (a / b));

        a += 5; // assignment operator
        System.out.println("After a += 5: " + a);
    }
}
